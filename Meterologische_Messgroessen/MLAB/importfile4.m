opts.VariableTypes = ["double", "datetime", "double", "double"];
opts = setvaropts(opts, 2, "InputFormat", "");

% Import the data
Winddaten = readtable(workbookFile, opts, "UseExcel", false);

for idx = 2:size(dataLines, 1)
    opts.DataRange = "A" + dataLines(idx, 1) + ":D" + dataLines(idx, 2);
    tb = readtable(workbookFile, opts, "UseExcel", false);
    Winddaten = [Winddaten; tb]; %#ok<AGROW>
end

end