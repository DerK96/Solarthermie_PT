%  WINDDATEN = IMPORTFILE1(FILE) reads data from the first worksheet in
%  the Microsoft Excel spreadsheet file named FILE.  Returns the data as
%  a table.
%
%  WINDDATEN = IMPORTFILE1(FILE, SHEET) reads from the specified
%  worksheet.
%
%  WINDDATEN = IMPORTFILE1(FILE, SHEET, DATALINES) reads from the
%  specified worksheet for the specified row interval(s). Specify
%  DATALINES as a positive scalar integer or a N-by-2 array of positive
%  scalar integers for dis-contiguous row intervals.
%
%  Example:
%  Winddaten = importfile1("C:\Users\Marvin\Documents\Solarthermie_PT\Meterologische_Messgroessen\200901_Winddaten.xlsx", "Tabelle1", [2, 86401]);
